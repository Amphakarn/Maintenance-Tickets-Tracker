const allPropertiesComparison = function (ticketsOrganizedByPropertyId, properties) {



  let final = [];
  //  ["Property Name", "Plumbing", "Electrical", "General Maintenance"]
  // ["Bridlewood", 1400, 3000, 1200]


  const costByMaintenanceType = function(maintenance_type_id, tickets) {
    let filterTickets = tickets.filter((ticket) => ticket.maintenance_type_id === maintenance_type_id);

    let costList = [];

    // filter out null values (null values are pending/inprogress)
    filterTickets.forEach(ticket => {
      if (ticket.actual_cost) {
        costList.push(ticket.actual_cost);
      }
    });


    //calculate sum
    let costSum = costList.reduce((a, b) => a + b, 0);

    return costSum;
  }

  const indPropertyData = function(propertyTickets) {
    let indProperty = [];
    // console.log('propertyTIckets ', propertyTickets);

  
    indProperty.push(propertyTickets.property_id);

    let maintenanceIds = [1, 2, 3];

    // for each maintenance type push the cost total for that id to the specific property arra
    maintenanceIds.forEach(id => indProperty.push(costByMaintenanceType(id, propertyTickets.ticketsArray)))

    return indProperty;
  }

  for (let i = 1; i < ticketsOrganizedByPropertyId.length; i++) {
    final.push(indPropertyData(ticketsOrganizedByPropertyId[i]));
  }

  for (let i = 0; i < properties.length; i++) {
    // console.log(properties[i].name); returns property ids from 
    // console.log(final[i][0]); returns name for propertie id in same position
    final[i][0] = properties[i].name
  }
  
  //adds to beginning of array
  final.unshift(["Property Name", "Plumbing", "Electrical", "General Maintenance"]);
  return final;

};

module.exports = { allPropertiesComparison };
